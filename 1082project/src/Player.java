
public class Player {
	
    public int life;
    public int ammo;
    public int shield;
    public int choice;

    public int getLife() {
        return life;
    }


    public void setLife(int life) {
        this.life = life;
    }


    public int getAmmo() {
        return ammo;
    }


    public void setAmmo(int ammo) {
        this.ammo = ammo;
    }


    public int getShield() {
        return shield;
    }


    public void setShield(int shield) {
        this.shield = shield;
    }

	
    public int getChoice() {
		return choice;
	}



	public void setChoice(int choice) {
		this.choice = choice;
	}

	

}
